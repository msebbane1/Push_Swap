#include "../includes/push_swap.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	while (str[i] == '-')
		i++;
	while ((str[i] >= '0' && str[i] <= '9') || str[i] == ' ')
		i++;
	if (str[i] != '\0')
		return (0);
	return (1);
}
